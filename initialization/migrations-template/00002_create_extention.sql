-- =============================================================
-- Миграция: 00002_create_extention.sql
-- Выполняется от: postgres (superuser)
-- Создаёт схему и сервисную роль с правами только в своей схеме.
-- Имена и пароль приходят из переменных окружения (.env -> atlas.hcl).
-- =============================================================

-- -------------------------------------------------------------
-- 1. СХЕМА
-- -------------------------------------------------------------
CREATE SCHEMA IF NOT EXISTS {{ .Schema }};

-- -------------------------------------------------------------
-- 2. РОЛЬ (пользователь БД)
-- LOGIN — может подключаться к БД.
-- NOCREATEDB, NOCREATEROLE — не может создавать БД и роли.
-- NOINHERIT — не наследует права других ролей.
-- -------------------------------------------------------------
DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1 FROM pg_roles WHERE rolname = '{{ .User }}'
    ) THEN
        CREATE ROLE {{ .User }}
            LOGIN
            PASSWORD '{{ .Password }}'
            NOCREATEDB
            NOCREATEROLE
            NOINHERIT;
    END IF;
END
$$;

-- -------------------------------------------------------------
-- 3. Владелец схемы
-- роль владеет всеми объектами в своей схеме.
-- -------------------------------------------------------------
ALTER SCHEMA {{ .Schema }} OWNER TO {{ .User }};

-- -------------------------------------------------------------
-- 4. Права на схему
-- USAGE — видит объекты внутри схемы.
-- CREATE — может создавать таблицы, индексы и т.п. в схеме.
-- -------------------------------------------------------------
GRANT USAGE, CREATE ON SCHEMA {{ .Schema }} TO {{ .User }};

-- -------------------------------------------------------------
-- 5. Права на уже существующие объекты
-- (на случай, если схема уже была с таблицами)
-- -------------------------------------------------------------
GRANT SELECT, INSERT, UPDATE, DELETE
    ON ALL TABLES IN SCHEMA {{ .Schema }} TO {{ .User }};

GRANT USAGE, SELECT
    ON ALL SEQUENCES IN SCHEMA {{ .Schema }} TO {{ .User }};

-- -------------------------------------------------------------
-- 6. DEFAULT PRIVILEGES
-- Все таблицы и последовательности, созданные ПОЗЖЕ в схеме,
-- автоматически получат нужные права для роли.
-- -------------------------------------------------------------
ALTER DEFAULT PRIVILEGES IN SCHEMA {{ .Schema }}
    GRANT SELECT, INSERT, UPDATE, DELETE ON TABLES TO {{ .User }};

ALTER DEFAULT PRIVILEGES IN SCHEMA {{ .Schema }}
    GRANT USAGE, SELECT ON SEQUENCES TO {{ .User }};

-- -------------------------------------------------------------
-- 7. search_path
-- роль работает только в своей схеме,
-- без явного указания схемы:
--   CREATE TABLE users (...) вместо schema.users
-- -------------------------------------------------------------
ALTER ROLE {{ .User }}
SET search_path = {{ .Schema }};

-- -------------------------------------------------------------
-- 8. Ограничение public
-- роль не может создавать объекты в public.
-- -------------------------------------------------------------
REVOKE ALL ON SCHEMA public FROM {{ .User }};